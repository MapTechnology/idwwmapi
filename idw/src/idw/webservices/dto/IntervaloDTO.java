package idw.webservices.dto;

public class IntervaloDTO {
	
	private long numero;
	private double lie;
	private double lse;
	private double pontoMedio;
	private long frequencia;
	private double amplitude;
	private double amplitudeClasse;
	
	public long getNumero() {
		return numero;
	}
	public void setNumero(long numero) {
		this.numero = numero;
	}
	public double getLie() {
		return lie;
	}
	public void setLie(double lie) {
		this.lie = lie;
	}
	public double getLse() {
		return lse;
	}
	public void setLse(double lse) {
		this.lse = lse;
	}
	public double getPontoMedio() {
		return pontoMedio;
	}
	public void setPontoMedio(double pontoMedio) {
		this.pontoMedio = pontoMedio;
	}
	public long getFrequencia() {
		return frequencia;
	}
	public void setFrequencia(long frequencia) {
		this.frequencia = frequencia;
	}
	public double getAmplitude() {
		return amplitude;
	}
	public void setAmplitude(double amplitude) {
		this.amplitude = amplitude;
	}
	public double getAmplitudeClasse() {
		return amplitudeClasse;
	}
	public void setAmplitudeClasse(double amplitudeClasse) {
		this.amplitudeClasse = amplitudeClasse;
	}	
	
	
}
